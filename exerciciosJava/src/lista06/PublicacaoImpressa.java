package lista06;

public class PublicacaoImpressa {

	private int codigo;
	
	private String titulo;
	
	private String editora;
	
	private String ano;

	public PublicacaoImpressa(int codigo, String titulo, String editora, String ano) {
		super();
		this.codigo = codigo;
		this.titulo = titulo;
		this.editora = editora;
		this.ano = ano;
	}

	
}
